let number = 0;

    function increaseNumber() {
        number++;
        document.getElementById("numberDisplay").textContent = number;  
    }

    function decreaseNumber() {
        number--;
        document.getElementById("numberDisplay").textContent = number;
    }

    function resetNumber() {
        number = 0;
        document.getElementById("numberDisplay").textContent = number;
    }

    

    
    
   